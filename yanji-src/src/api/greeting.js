function pick(arr) {
  return arr[Math.floor(Math.random() * arr.length)]
}

// 按当下时间抽一条开屏语；特殊日子优先，周末上午混入周末池
export function pickGreeting(now = new Date()) {
  const mmdd = `${String(now.getMonth() + 1).padStart(2, '0')}-${String(now.getDate()).padStart(2, '0')}`
  if (SPECIAL[mmdd]) return pick(SPECIAL[mmdd])
  const h = now.getHours() + now.getMinutes() / 60
  const day = now.getDay()
  let pool = BUCKETS.find((b) => h >= b.from && h < b.to)?.lines || []
  if ((day === 0 || day === 6) && h >= 7 && h < 12) pool = pool.concat(WEEKEND)
  return pool.length ? pick(pool) : null
}
